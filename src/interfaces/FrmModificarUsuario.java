    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if (!this.txtUsuario.getText().replaceAll(" ", "").equals("")
            && !this.txtCi.getText().replaceAll(" ", "").equals("")
            && !this.txtNombre.getText().replaceAll(" ", "").equals("")
            && !this.txtApellido.getText().replaceAll(" ", "").equals("")) {
            if (new MetodosSQL().updateUsuario(usuario.usuario, txtUsuario.getText(), this.txtNombre.getText(),
                    this.txtApellido.getText())) {
                JOptionPane.showMessageDialog(null, "Usuario modificado correctamente.");
                new FrmGestionUsuarios(this.usuario).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "El nombre de usuario ya existe.");
            }
            
        }
    }//GEN-LAST:event_btnCrearActionPerformed

