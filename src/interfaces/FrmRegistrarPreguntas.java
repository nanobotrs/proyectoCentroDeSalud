    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!this.txtR1.getText().replaceAll(" ", "").equals("") && !this.txtR2.getText().replaceAll(" ", "").equals("")
                && !this.txtR3.getText().replaceAll(" ", "").equals("")
                && !this.txtR4.getText().replaceAll(" ", "").equals("")) {
            this.setPreguntas();
            new MetodosSQL().crearUsuario(this.usuario);
            JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");
            this.dispose();
            new FrmGestionUsuarios().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Campos vacios");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

